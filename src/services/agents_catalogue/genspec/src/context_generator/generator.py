"""
Documentation generator module for Context Generator.

This module provides functionality to generate comprehensive documentation
based on the analysis results from the CodebaseAnalyzer.
"""

import os
import json
from typing import Dict, List, Any, Optional
import logging
from pathlib import Path

logger = logging.getLogger(__name__)


class DocumentationGenerator:
    """
    Generates comprehensive documentation based on codebase analysis.
    """
    
    def __init__(self, analysis_results: Dict[str, Any], output_dir: str = "docs"):
        """
        Initialize the documentation generator.
        
        Args:
            analysis_results: Results from the CodebaseAnalyzer
            output_dir: Directory to output generated documentation
        """
        self.analysis_results = analysis_results
        self.output_dir = output_dir
        
        # Ensure output directory exists
        os.makedirs(output_dir, exist_ok=True)
    
    def generate_all(self) -> Dict[str, str]:
        """
        Generate all documentation files.
        
        Returns:
            Dict mapping file paths to their generated content
        """
        generated_files = {}
        
        # Generate README
        readme_path = os.path.join(self.output_dir, "README.md")
        readme_content = self.generate_readme()
        self._write_file(readme_path, readme_content)
        generated_files["README.md"] = readme_content
        
        # Generate structure documentation
        structure_path = os.path.join(self.output_dir, "structure.md")
        structure_content = self.generate_structure_doc()
        self._write_file(structure_path, structure_content)
        generated_files["structure.md"] = structure_content
        
        # Generate flows documentation
        flows_path = os.path.join(self.output_dir, "flows.md")
        flows_content = self.generate_flows_doc()
        self._write_file(flows_path, flows_content)
        generated_files["flows.md"] = flows_content
        
        # Generate API documentation
        api_path = os.path.join(self.output_dir, "apis.md")
        api_content = self.generate_api_doc()
        self._write_file(api_path, api_content)
        generated_files["apis.md"] = api_content
        
        # Generate jobs documentation
        jobs_path = os.path.join(self.output_dir, "jobs.md")
        jobs_content = self.generate_jobs_doc()
        self._write_file(jobs_path, jobs_content)
        generated_files["jobs.md"] = jobs_content
        
        # Generate failure points documentation
        failure_path = os.path.join(self.output_dir, "failure_points.md")
        failure_content = self.generate_failure_points_doc()
        self._write_file(failure_path, failure_content)
        generated_files["failure_points.md"] = failure_content
        
        # Generate retry and idempotency documentation
        retry_path = os.path.join(self.output_dir, "retry_idempotency.md")
        retry_content = self.generate_retry_idempotency_doc()
        self._write_file(retry_path, retry_content)
        generated_files["retry_idempotency.md"] = retry_content
        
        return generated_files
    
